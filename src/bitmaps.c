const GFX_Bitmap *Bitmap_GetGlyph(char ch)
{
    if ((ch >= 'A') && (ch <= 'Z'))
    {
        return &GLYPHS_A_Z[(uint8_t)(ch - 'A')];
    }

    if ((ch >= '0') && (ch <= '9'))
    {
        return &GLYPHS_0_9[(uint8_t)(ch - '0')];
    }

    return NULL;
}

